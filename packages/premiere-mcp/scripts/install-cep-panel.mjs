import { access, cp, mkdir, readFile, readdir, rm, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

export const DEFAULT_BRIDGE_DIR = "C:/pr-mcp-cmd";
export const DEFAULT_EXTENSION_ID = "com.pr.mcp.panel";

export function normalizeBridgeDir(value) {
  const normalized = String(value ?? "")
    .trim()
    .replace(/\\/g, "/")
    .replace(/[\\/]+$/, "");

  if (!normalized) {
    throw new Error("bridge directory must not be empty");
  }

  return normalized;
}

export function buildBridgeConfigSource(bridgeDir) {
  const normalized = normalizeBridgeDir(bridgeDir);

  return [
    "(function (global) {",
    '  "use strict";',
    "",
    "  // Generated by scripts/install-cep-panel.mjs.",
    `  global.__PR_MCP_BRIDGE_DIR__ = ${JSON.stringify(normalized)};`,
    "})(typeof globalThis !== \"undefined\" ? globalThis : this);",
    "",
  ].join("\n");
}

export function getRecommendedMcpEnv(bridgeDir) {
  const normalized = normalizeBridgeDir(bridgeDir);

  return {
    PREMIERE_MCP_COMMAND_FILE: `${normalized}/cmd.json`,
    PREMIERE_MCP_RESULT_FILE: `${normalized}/result.json`,
  };
}

export function detectUserCepExtensionsDir(env = process.env) {
  const appData = String(env.APPDATA ?? "").trim();

  if (!appData) {
    return "";
  }

  return path.join(appData, "Adobe", "CEP", "extensions");
}

async function ensurePathExists(filePath, label) {
  try {
    await access(filePath);
  } catch {
    throw new Error(`${label} not found: ${filePath}`);
  }
}

function parseBundleId(manifestSource) {
  const matched = String(manifestSource || "").match(
    /ExtensionBundleId\s*=\s*"([^"]+)"/i,
  );
  return matched ? String(matched[1] || "").trim() : "";
}

async function readBundleIdFromManifest(manifestPath) {
  try {
    const source = await readFile(manifestPath, "utf8");
    return parseBundleId(source);
  } catch {
    return "";
  }
}

export async function detectConflictingBundleDirs({
  extensionsDir,
  bundleId,
  targetDir,
} = {}) {
  const normalizedBundleId = String(bundleId || "").trim();

  if (!extensionsDir || !normalizedBundleId) {
    return [];
  }

  const entries = await readdir(extensionsDir, { withFileTypes: true }).catch(
    () => [],
  );
  const targetPath = path.resolve(String(targetDir || ""));
  const conflicts = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) {
      continue;
    }

    const dirPath = path.join(extensionsDir, entry.name);
    if (targetPath && path.resolve(dirPath) === targetPath) {
      continue;
    }

    const manifestPath = path.join(dirPath, "CSXS", "manifest.xml");
    const foundBundleId = await readBundleIdFromManifest(manifestPath);

    if (foundBundleId === normalizedBundleId) {
      conflicts.push(dirPath);
    }
  }

  return conflicts.sort((left, right) => left.localeCompare(right));
}

export async function installCepPanel({
  projectDir = process.cwd(),
  extensionsDir = detectUserCepExtensionsDir(),
  extensionId = DEFAULT_EXTENSION_ID,
  bridgeDir = DEFAULT_BRIDGE_DIR,
} = {}) {
  if (!extensionsDir) {
    throw new Error("extensions directory is required");
  }

  const sourceDir = path.join(projectDir, "cep-panel");
  const targetDir = path.join(extensionsDir, extensionId);
  const normalizedBridgeDir = normalizeBridgeDir(bridgeDir);
  const bridgeConfigPath = path.join(targetDir, "js", "bridge-config.js");

  await ensurePathExists(sourceDir, "cep-panel source directory");
  await ensurePathExists(path.join(sourceDir, "index.html"), "CEP panel entry");
  await ensurePathExists(path.join(sourceDir, "js", "panel.js"), "CEP panel script");
  await ensurePathExists(
    path.join(sourceDir, "CSXS", "manifest.xml"),
    "CEP manifest",
  );

  await mkdir(extensionsDir, { recursive: true });
  await rm(targetDir, { recursive: true, force: true });
  await cp(sourceDir, targetDir, { recursive: true });
  await writeFile(
    bridgeConfigPath,
    buildBridgeConfigSource(normalizedBridgeDir),
    "utf8",
  );

  const conflicts = await detectConflictingBundleDirs({
    extensionsDir,
    bundleId: extensionId,
    targetDir,
  });

  return {
    bridgeDir: normalizedBridgeDir,
    conflicts,
    extensionsDir,
    sourceDir,
    targetDir,
    extensionId,
    bridgeConfigPath,
    mcpEnv: getRecommendedMcpEnv(normalizedBridgeDir),
  };
}

function parseArgs(argv) {
  const options = {};

  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];

    if (token === "--help" || token === "-h") {
      options.help = true;
      continue;
    }

    if (!token.startsWith("--")) {
      throw new Error(`unknown argument: ${token}`);
    }

    const nextValue = argv[index + 1];

    if (typeof nextValue !== "string" || nextValue.startsWith("--")) {
      throw new Error(`missing value for argument: ${token}`);
    }

    if (token === "--project-dir") {
      options.projectDir = nextValue;
    } else if (token === "--extensions-dir") {
      options.extensionsDir = nextValue;
    } else if (token === "--bridge-dir") {
      options.bridgeDir = nextValue;
    } else if (token === "--extension-id") {
      options.extensionId = nextValue;
    } else {
      throw new Error(`unknown argument: ${token}`);
    }

    index += 1;
  }

  return options;
}

function printHelp() {
  console.log(`Usage:
  node scripts/install-cep-panel.mjs [options]

Options:
  --bridge-dir <path>       Bridge directory written into bridge-config.js
  --extensions-dir <path>   CEP extensions directory to install into
  --project-dir <path>      Project root that contains cep-panel/
  --extension-id <id>       CEP extension directory name
  --help                    Show this help
`);
}

async function main() {
  const options = parseArgs(process.argv.slice(2));

  if (options.help) {
    printHelp();
    return;
  }

  const result = await installCepPanel(options);

  console.log(`Installed CEP panel to: ${result.targetDir}`);
  console.log(`Bridge config written to: ${result.bridgeConfigPath}`);
  if (result.conflicts.length > 0) {
    console.warn(
      "Detected conflicting CEP bundle directories (same ExtensionBundleId):",
    );
    for (const dir of result.conflicts) {
      console.warn(`- ${dir}`);
    }
    console.warn(
      "Conflicts can cause Premiere to load stale panel code. Disable backup manifests or move those folders out of the CEP extensions directory.",
    );
  }
  console.log("Recommended MCP environment variables:");
  console.log(JSON.stringify(result.mcpEnv, null, 2));
}

const isEntrypoint =
  process.argv[1] &&
  path.resolve(process.argv[1]) === path.resolve(fileURLToPath(import.meta.url));

if (isEntrypoint) {
  main().catch((error) => {
    console.error(`CEP panel install failed: ${error instanceof Error ? error.message : String(error)}`);
    process.exit(1);
  });
}
